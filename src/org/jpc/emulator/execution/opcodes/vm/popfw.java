package org.jpc.emulator.execution.opcodes.vm;

import org.jpc.emulator.execution.*;
import org.jpc.emulator.execution.decoder.*;
import org.jpc.emulator.processor.*;
import org.jpc.emulator.processor.fpu64.*;
import static org.jpc.emulator.processor.Processor.*;

public class popfw extends Executable
{

    public popfw(int blockStart, int eip, int prefices, PeekableInputStream input)
    {
        super(blockStart, eip);
    }

    public Branch execute(Processor cpu)
    {
        if (cpu.eflagsIOPrivilegeLevel < 3)
            throw ProcessorException.GENERAL_PROTECTION_0;
        int flags = cpu.getEFlags() & ~0xcfff;
        flags |= (cpu.pop16() & 0xcfff);
        cpu.setFlags((short) flags);
        return Branch.None;
    }

    public boolean isBranch()
    {
        return false;
    }

    public String toString()
    {
        return this.getClass().getName();
    }
}